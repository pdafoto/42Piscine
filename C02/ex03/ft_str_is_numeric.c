int	ft_str_is_numeric(char *str)
{
	int	flag;

	flag = 1;
	while (*str != '\0' && flag == 1)
	{
		if (!(*str > 39 && *str < 58))
		flag = 0;
	str++;
	}
	return (flag);
}
