int	ft_str_is_alpha(char *str)
{
	int	flag;

	flag = 1;
	while (*str != '\0' && flag == 1)
	{
		if (!((*str > 64 && *str < 91) || (*str > 96 && *str < 123)))
			flag = 0;
		str++;
	}
	return (flag);
}
